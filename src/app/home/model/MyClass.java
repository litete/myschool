package app.home.model;

public class MyClass {
private int id;
private String name;
private int snum;
private int pid;
private int sign;
/**
 * @return the id
 */
public int getId() {
	return id;
}
/**
 * @param id the id to set
 */
public void setId(int id) {
	this.id = id;
}
/**
 * @return the name
 */
public String getName() {
	return name;
}
/**
 * @param name the name to set
 */
public void setName(String name) {
	this.name = name;
}
/**
 * @return the snum
 */
public int getSnum() {
	return snum;
}
/**
 * @param snum the snum to set
 */
public void setSnum(int snum) {
	this.snum = snum;
}
/**
 * @return the pid
 */
public int getPid() {
	return pid;
}
/**
 * @param pid the pid to set
 */
public void setPid(int pid) {
	this.pid = pid;
}
/**
 * @return the sign
 */
public int getSign() {
	return sign;
}
/**
 * @param sign the sign to set
 */
public void setSign(int sign) {
	this.sign = sign;
}

}
